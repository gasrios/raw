/// # Errors
///
/// If the number is not a valid type in TIFF
#[must_use]
pub fn type_size(type_: u16) -> usize {
    match type_ {
        ASCII | BYTE | SBYTE | UNDEFINED => 1,
        SHORT | SSHORT => 2,
        FLOAT | LONG | SLONG => 4,
        DOUBLE => 8,
        _ => 0,
    }
}

